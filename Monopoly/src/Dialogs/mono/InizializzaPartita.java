    private void AssegnaButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_AssegnaButtonActionPerformed
        if(!ContrattoField.getText().trim().isEmpty()){
            try{
                myconObj=DriverManager.getConnection("jdbc:oracle:thin:@localhost:1521:XE", "Utente","12345");
                
                //controllo che il contratto esiste
                Statement cop=myconObj.createStatement();
                ResultSet contrp=cop.executeQuery("Select Nome FROM Contratti WHERE Nome='"+ContrattoField.getText()+"'");
                if(contrp.next()){
                    //Controllo che il contratto scelto non appartenga a nessuno 
                    Statement co=myconObj.createStatement();
                    ResultSet contr=co.executeQuery("SELECT Pedina FROM Contratti WHERE IDPartita="+IDPartita+"and Nome='"+ContrattoField.getText()+"'");
                    if(!contr.next()){
                        JOptionPane.showMessageDialog(this, "Assegnare un contratto a"+Partecipante+" che non appartiene gi√† a qualcuno");
                    }else{
                        Statement tot=myconObj.createStatement();
                        tot.executeUpdate("INSERT INTO MOSSA VALUES (0,0,0,0,0,1,null,'"+Partecipante+"',"+IDPartita+",null,null,null,'"+ContrattoField.getText()+"',null,"+IDPartita+","+IDPartita+",null,"+(NumContratto-1)+")");
                        this.dispose();
                    }
                }else{
                    JOptionPane.showMessageDialog(this, "Il contratto non esiste");
                }
            }
            catch(SQLException e){e.printStackTrace();}
        }else{
            JOptionPane.showMessageDialog(this, "Assegnare un contratto a"+Partecipante);
        }
    }//GEN-LAST:event_AssegnaButtonActionPerformed

